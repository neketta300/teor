import pandas as pd
import numpy as np

# ============================================================
# ЧАСТЬ 1: Создание DataFrame
# ============================================================

print("=" * 60)
print("ЧАСТЬ 1: СОЗДАНИЕ DATAFRAME")
print("=" * 60)

# Из словаря
data = {
    'имя': ['Анна', 'Борис', 'Вика', 'Глеб'],
    'возраст': [25, 30, 35, 28],
    'город': ['Минск', 'Гомель', 'Минск', 'Брест'],
    'зарплата': [2500, 3000, 3500, 2800]
}

df = pd.DataFrame(data)
print("DataFrame из словаря:")
print(df)

# Чтение из CSV (на олимпиаде данные будут в CSV!)
# df = pd.read_csv('data.csv')
# df = pd.read_excel('data.xlsx')

# ============================================================
# ЧАСТЬ 2: Обзор данных
# ============================================================

print("\n" + "=" * 60)
print("ЧАСТЬ 2: ОБЗОР ДАННЫХ")
print("=" * 60)

print(f"\nПервые 3 строки:\n{df.head(3)}")
print(f"\nПоследние 2 строки:\n{df.tail(2)}")
print(f"\nИнформация о данных:")
print(df.info())
print(f"\nСтатистика:")
print(df.describe())
print(f"\nРазмер: {df.shape}")  # (строки, столбцы)
print(f"Столбцы: {df.columns.tolist()}")
print(f"Типы данных:\n{df.dtypes}")

# ============================================================
# ЧАСТЬ 3: Выбор данных
# ============================================================

print("\n" + "=" * 60)
print("ЧАСТЬ 3: ВЫБОР ДАННЫХ")
print("=" * 60)

# Один столбец (возвращает Series)
print(f"\nСтолбец 'имя':\n{df['имя']}")

# Несколько столбцов
print(f"\nСтолбцы 'имя' и 'город':\n{df[['имя', 'город']]}")

# Строки по индексу
print(f"\nПервая строка (iloc[0]):\n{df.iloc[0]}")
print(f"\nСтроки 1-2 (iloc[1:3]):\n{df.iloc[1:3]}")

# Строки и столбцы по индексу
print(f"\nСтроки 0-1, столбцы 1-2:\n{df.iloc[0:2, 1:3]}")

# ============================================================
# ЧАСТЬ 4: Фильтрация (ОЧЕНЬ ВАЖНО!)
# ============================================================

print("\n" + "=" * 60)
print("ЧАСТЬ 4: ФИЛЬТРАЦИЯ")
print("=" * 60)

# Фильтр по условию
minsk = df[df['город'] == 'Минск']
print(f"\nИз Минска:\n{minsk}")

# Несколько условий (& = and, | = or)
high_salary = df[(df['зарплата'] > 2800) & (df['возраст'] < 35)]
print(f"\nЗарплата > 2800 И возраст < 35:\n{high_salary}")

# Фильтр по списку значений
cities = df[df['город'].isin(['Минск', 'Брест'])]
print(f"\nГорода Минск или Брест:\n{cities}")

# ============================================================
# ЧАСТЬ 5: Создание новых столбцов
# ============================================================

print("\n" + "=" * 60)
print("ЧАСТЬ 5: НОВЫЕ СТОЛБЦЫ")
print("=" * 60)

# Новый столбец
df['зарплата_usd'] = df['зарплата'] / 3.2  # Курс доллара
print(f"\nС зарплатой в USD:\n{df}")

# Столбец с условием
df['опытный'] = df['возраст'] >= 30
print(f"\nС флагом 'опытный':\n{df}")

# Столбец через функцию
df['имя_длина'] = df['имя'].apply(len)
print(f"\nС длиной имени:\n{df}")

# ============================================================
# ЧАСТЬ 6: Группировка и агрегация
# ============================================================

print("\n" + "=" * 60)
print("ЧАСТЬ 6: ГРУППИРОВКА")
print("=" * 60)

# Средняя зарплата по городам
city_stats = df.groupby('город')['зарплата'].mean()
print(f"\nСредняя зарплата по городам:\n{city_stats}")

# Несколько агрегаций
city_agg = df.groupby('город').agg({
    'зарплата': ['mean', 'min', 'max', 'count'],
    'возраст': 'mean'
})
print(f"\nПолная статистика по городам:\n{city_agg}")

# ============================================================
# ЧАСТЬ 7: Сортировка
# ============================================================

print("\n" + "=" * 60)
print("ЧАСТЬ 7: СОРТИРОВКА")
print("=" * 60)

# По одному столбцу
sorted_salary = df.sort_values('зарплата', ascending=False)
print(f"\nПо убыванию зарплаты:\n{sorted_salary}")

# По нескольким столбцам
sorted_multi = df.sort_values(['город', 'возраст'])
print(f"\nПо городу, затем по возрасту:\n{sorted_multi}")

# ============================================================
# ЧАСТЬ 8: Работа с пропущенными данными
# ============================================================

print("\n" + "=" * 60)
print("ЧАСТЬ 8: ПРОПУЩЕННЫЕ ДАННЫЕ")
print("=" * 60)

# DataFrame с пропусками
df_missing = pd.DataFrame({
    'A': [1, 2, np.nan, 4, 5],
    'B': [10, np.nan, np.nan, 40, 50],
    'C': [100, 200, 300, 400, 500]
})
print(f"Данные с пропусками:\n{df_missing}")

# Проверка пропусков
print(f"\nПропуски:\n{df_missing.isna()}")
print(f"\nСумма пропусков по столбцам:\n{df_missing.isna().sum()}")

# Удаление строк с пропусками
df_dropped = df_missing.dropna()
print(f"\nПосле удаления пропусков:\n{df_dropped}")

# Заполнение пропусков
df_filled_mean = df_missing.fillna(df_missing.mean())
print(f"\nЗаполнение средним:\n{df_filled_mean}")

df_filled_forward = df_missing.fillna(method='ffill')
print(f"\nЗаполнение вперёд (последним значением):\n{df_filled_forward}")

# ============================================================
# ЧАСТЬ 9: Объединение DataFrame
# ============================================================

print("\n" + "=" * 60)
print("ЧАСТЬ 9: ОБЪЕДИНЕНИЕ")
print("=" * 60)

df1 = pd.DataFrame({
    'id': [1, 2, 3],
    'имя': ['Анна', 'Борис', 'Вика']
})

df2 = pd.DataFrame({
    'id': [2, 3, 4],
    'зарплата': [3000, 3500, 4000]
})

# INNER JOIN (только общие)
inner = pd.merge(df1, df2, on='id', how='inner')
print(f"\nINNER JOIN:\n{inner}")

# LEFT JOIN (все из левого)
left = pd.merge(df1, df2, on='id', how='left')
print(f"\nLEFT JOIN:\n{left}")

# OUTER JOIN (все)
outer = pd.merge(df1, df2, on='id', how='outer')
print(f"\nOUTER JOIN:\n{outer}")

# ============================================================
# ЧАСТЬ 10: Практические ML-задачи
# ============================================================

print("\n" + "=" * 60)
print("ЧАСТЬ 10: ПРАКТИЧЕСКИЕ ML-ЗАДАЧИ")
print("=" * 60)

# --- Задача 1: Загрузка и обзор датасета ---
print("\n--- Создание учебного датасета ---")
np.random.seed(42)

df_ml = pd.DataFrame({
    'площадь': np.random.randint(30, 150, 100),
    'комнаты': np.random.randint(1, 5, 100),
    'этаж': np.random.randint(1, 20, 100),
    'возраст_дома': np.random.randint(0, 50, 100)
})

# Цена зависит от признаков + шум
df_ml['цена'] = (
    df_ml['площадь'] * 50 +
    df_ml['комнаты'] * 5000 +
    df_ml['этаж'] * 1000 -
    df_ml['возраст_дома'] * 500 +
    np.random.randn(100) * 10000  # Шум
)

print(f"Размер: {df_ml.shape}")
print(f"\nПервые 5 строк:\n{df_ml.head()}")
print(f"\nСтатистика:\n{df_ml.describe()}")

# --- Задача 2: Корреляция признаков ---
print("\n--- Корреляция ---")
corr = df_ml.corr()
print(f"\nКорреляционная матрица:\n{corr}")
print("\nКорреляция с целевой переменной 'цена':")
print(corr['цена'].sort_values(ascending=False))

# --- Задача 3: Кодирование категориальных признаков ---
print("\n--- One-Hot Encoding ---")
df_cat = pd.DataFrame({
    'город': ['Минск', 'Гомель', 'Минск', 'Брест', 'Гомель'],
    'зарплата': [2500, 2000, 3000, 2200, 2800]
})
print(f"Исходные:\n{df_cat}")

df_encoded = pd.get_dummies(df_cat, columns=['город'], dtype=int)
print(f"\nПосле One-Hot Encoding:\n{df_encoded}")

# --- Задача 4: Биннинг (разбиение на категории) ---
print("\n--- Биннинг ---")
df_binned = df_ml.copy()
df_binned['площадь_кат'] = pd.cut(
    df_binned['площадь'],
    bins=[0, 50, 100, 150],
    labels=['малая', 'средняя', 'большая']
)
print(f"Биннинг площади:\n{df_binned[['площадь', 'площадь_кат']].head(10)}")

# --- Задача 5: Подготовка X и y для ML ---
print("\n--- Разделение на признаки (X) и целевую переменную (y) ---")
X = df_ml[['площадь', 'комнаты', 'этаж', 'возраст_дома']]
y = df_ml['цена']

print(f"X shape: {X.shape}")
print(f"y shape: {y.shape}")
print(f"\nПервые 5 строк X:\n{X.head()}")
print(f"\nПервые 5 значений y:\n{y.head()}")

